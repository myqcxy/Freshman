/*4.古代《张丘建算经》中有一道百鸡问题：鸡翁一，值钱五；母鸡一，值钱三；鸡雏三，值钱一。百钱买百鸡，问鸡翁、母、雏各几何？其意为：公鸡每只5元，母鸡每只3元，小鸡3只1元。请用穷举法编程计算，若用100元买100只鸡，则公鸡、母鸡和小鸡各能买多少只。
输出样例：0  25  75
		  4  18  78
		  8  11  81
		  12  4  84
*/
#include<stdio.h>
int main()
{
	int a, b, c;
	for(c=0;c<=100;c++)
	{
		if(c%3==0)
		{
			a=(c/3)*4-100;
			b=100-a-c;
			if(a>=0&&b>=0)
			{
			printf("%d %d %d\n", a, b, c);
		    }
		}
    }  
	return 0;
}

